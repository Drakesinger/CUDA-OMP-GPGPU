AdvancedImage* AdvancedProvider::createGL(void)
    {
    Animable_I* ptrMOO=AdvancedProvider::createMOO();

    return new AdvancedImage(ptrMOO);
    }

Animable_I* AdvancedProvider::createMOO(void)
    {
    int dw = 16 * 60 *2 ; // =32*30=960
    int dh = 16 * 60; // =32*30=960

    float dt=1;

    return new AdvancedMOO(dw, dh,dt);
    }

