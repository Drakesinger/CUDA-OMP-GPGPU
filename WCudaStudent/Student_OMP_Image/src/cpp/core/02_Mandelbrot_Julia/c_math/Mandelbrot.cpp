int Mandelbrot::computeIndiceArret(double x, double y)
    {
    int k = 0;
    double a = x;
    double b = y;
    while (!isDivergent(a, b) && k <= this->n)
	{
	double aCopy = a;
	a = (a * a - b * b) + x;
	b = 2 * aCopy * b + y;
	k += 1;
	}
    return k;
    }

