ImageFonctionel* MandelbrotJuliaProvider::createGL(void)
    {
    AnimableFonctionel_I* ptrMOO = MandelbrotJuliaProvider::createMOO();

    return new ImageFonctionel(ptrMOO);
    }

AnimableFonctionel_I* MandelbrotJuliaProvider::createMOO(void)
    {
    int dw = 16 * 60 * 2; // =32*30=960
    int dh = 16 * 60; // =32*30=960

    int n = 50;

    return new MandelbrotJuliaMOO(dw, dh, n);
    }

