WarmupImage* WarmupProvider::createGL(void)
    {
    int dw = 16 * 60; // =32*30=960
    int dh = 16 * 60; // =32*30=960

    float dt = 2 * PI / 200; // animation para

    return new WarmupImage(dw, dh, dt);
    }

Animable_I* WarmupProvider::createMOO(void)
    {
    int dw = 16 * 60; // =32*30=960
    int dh = 16 * 60; // =32*30=960

    float dt = 2 * PI / 200; // animation para

    return new WarmupMOO(dw, dh, dt);
    }

