bool useMonteCarlo(void)
    {
    cout << endl << "[MonteCarlo]" << endl;

    bool isOk = true;

    int n = INT_MAX/1000;
    MonteCarlo mc(n);
    mc.process();

    isOk &= MathTools::isEquals(mc.getPi(), M_PI, 1e-3);

    return true;
    }

