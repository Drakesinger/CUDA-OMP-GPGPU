Advanced* AdvancedProvider::create()
    {
    int dw = 16 * 60 *2; // =32*30=960
    int dh = 16 * 60; // =32*30=960

    float dt=1;

    return new Advanced(dw, dh,dt);
    }

AdvancedImage* AdvancedProvider::createGL(void)
     {
     return new AdvancedImage(create());
     }

