ImageFonctionel* DamierProvider::createGL(void)
    {
    AnimableFonctionel_I*  ptrMOO=DamierProvider::createMOO();

    return new ImageFonctionel(ptrMOO);
    }

AnimableFonctionel_I* DamierProvider::createMOO(void)
    {
    int dw = 16 * 60 *2; // =32*30=960
    int dh = 16 * 60; // =32*30=960

    int n = 2;

    // animation para
    float dt = 2 * PI / 400;

    return new DamierMOO(dw, dh, dt, n);
    }

